export const createAccountAndSendTransaction = async () => {
    const account = privateKeyToAccount(privateKey)
    const nexusClient = await createNexusClient({
        signer: account,
        chain: baseSepolia,
        transport: http(),
        bundlerTransport: http(bundlerUrl),
    });
    const address = nexusClient.account.address;
    console.log("address", address)
    try {
        const hash = await nexusClient.sendTransaction({ calls: [{to : usdc, value: parseEther('0'), data: encodeFunctionData({ abi: erc20Abi, functionName: 'approve', args: [paymaster, parseEther('1000000')] })}] });
        console.log("Transaction hash: ", hash);
        const receipt = await nexusClient.waitForTransactionReceipt({ hash });
        return {address , hash};
    }
    catch (error) {
        console.log(error)
    }
}

